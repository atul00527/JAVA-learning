public class TypeConversion{
    public static void main(String[] args) {
        float myFloat = 5;
        System.out.println(myFloat);

        int myInt =(int) 5.45f;  //float min number leta hai ex. chote box me badi chij nhi rj=kha skte hai, vaise he float hai, aur to ya uske sath data type rkhte hai
        System.out.println(myInt);
    }
}