public class UnaryOperators {
    public static void main(String[] args) {
        int a = 5;

        System.out.println(a++);
        System.out.println(a);         /// increment

        int b = 9;

        System.out.println(b--);
        System.out.println(b);         // decrement

        System.out.println(--b);
        System.out.println(b);



    }
}
